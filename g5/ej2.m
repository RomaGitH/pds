fm = 1000;


h1 = @(z) 1./(1-1/2.*(z.^-1)+ 1/4.*(z.^-2));
h2 = @(z) (z.^-1)./(1-(z.^-1)-(z.^-2));
h3 = @(z) 7./(1-2.*(z.^-1)+6.*(z.^-2));
h4 = @(z) 1+(1/2.*(z.^-1))+1/4.*(z.^-2)+1/8.*(z.^-3)+1/16.*(z.^-4)+1/32.*(z.^-5)+1/64.*(z.^-6)+1/128.*(z.^-7);


w = 0:0.1: pi;
##w = 0:100: 10000;
H1 = abs(h1(exp((1i.*w))));
H2 = abs(h2(exp((1i.*w))));
H3 = abs(h3(exp((1i.*w))));
H4 = abs(h4(exp((1i.*w))));

subplot(2,2,1);
stem(w,H1);
title('H(z) = 1/(1-1/2*(z^-1)+ 1/4*(z^-2))');
subplot(2,2,2);
stem(w,H2);
title('H(z) = (z^-1)/(1-(z^-1)-(z^-2))');
subplot(2,2,3);
stem(w,H3);
title('H(z) = 7/(1-2*(z^-1)+6*(z^-2))');
subplot(2,2,4);
stem(w,H4);
title('H(z) =  1+1/2*(z^-1)+1/4*(z^-2) + ... + 1/128.*(z.^-7)');
